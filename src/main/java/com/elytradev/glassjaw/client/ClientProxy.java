package com.elytradev.glassjaw.client;

import com.elytradev.glassjaw.GlassJaw;
import com.elytradev.glassjaw.Proxy;

import net.minecraft.client.renderer.block.model.ModelResourceLocation;
import net.minecraft.util.ResourceLocation;
import net.minecraftforge.client.event.ModelRegistryEvent;
import net.minecraftforge.client.model.ModelLoader;
import net.minecraftforge.fml.common.eventhandler.SubscribeEvent;

public class ClientProxy extends Proxy {
	@SubscribeEvent
	public void registerItemModels(ModelRegistryEvent event) {
		ModelLoader.setCustomModelResourceLocation(GlassJaw.GLASS_DAGGER, 0, new ModelResourceLocation(new ResourceLocation(GlassJaw.MODID, "glassdagger"), "inventory"));
	}
}
